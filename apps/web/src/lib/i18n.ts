const BENGALI_DIGIT_TO_ARABIC: Record<string, string> = {
  "০": "0",
  "১": "1",
  "২": "2",
  "৩": "3",
  "৪": "4",
  "৫": "5",
  "৬": "6",
  "৭": "7",
  "৮": "8",
  "৯": "9",
};

export function localizedDuration(duration: string, locale: Locale): string {
  if (locale === "bn") return duration;
  return duration.replace(
    /[০-৯]/g,
    (digit) => BENGALI_DIGIT_TO_ARABIC[digit],
  );
}

const ARABIC_TO_BENGALI: Record<string, string> = {
  "0": "০",
  "1": "১",
  "2": "২",
  "3": "৩",
  "4": "৪",
  "5": "৫",
  "6": "৬",
  "7": "৭",
  "8": "৮",
  "9": "৯",
};

export function toLocaleDigits(value: string | number, locale: Locale): string {
  const s = String(value);
  return locale === "en" ? s : s.replace(/[0-9]/g, (d) => ARABIC_TO_BENGALI[d]);
}

// Relative time from an ISO date: "just now / X min / X hr / X days ago",
// falling back to an absolute date after a week.
export function relativeTime(value: string, locale: Locale): string {
  const d = new Date(value);
  if (isNaN(d.getTime())) return value; // non-ISO (mock) strings unchanged
  const diff = Date.now() - d.getTime();
  const min = Math.floor(diff / 60000);

  if (min < 1) return locale === "bn" ? "এইমাত্র" : "just now";
  if (min < 60)
    return locale === "bn"
      ? `${toLocaleDigits(min, locale)} মিনিট আগে`
      : `${min} min ago`;

  const hr = Math.floor(min / 60);
  if (hr < 24)
    return locale === "bn"
      ? `${toLocaleDigits(hr, locale)} ঘণ্টা আগে`
      : `${hr} hr ago`;

  const day = Math.floor(hr / 24);
  if (day < 7)
    return locale === "bn"
      ? `${toLocaleDigits(day, locale)} দিন আগে`
      : `${day} day${day > 1 ? "s" : ""} ago`;

  return d.toLocaleDateString(locale === "bn" ? "bn-BD" : "en-GB", {
    day: "numeric",
    month: "long",
    year: "numeric",
  });
}

export function localizedPublishedAt(publishedAt: string, locale: Locale): string {
  // API dates are ISO strings — format as a localized date (deterministic,
  // avoids hydration mismatch).
  if (/\d{4}-\d{2}-\d{2}T/.test(publishedAt)) {
    const d = new Date(publishedAt);
    if (!isNaN(d.getTime())) {
      return d.toLocaleDateString(locale === "bn" ? "bn-BD" : "en-GB", {
        day: "numeric",
        month: "long",
        year: "numeric",
      });
    }
  }

  if (locale === "bn") return publishedAt;
  const arabic = publishedAt.replace(
    /[০-৯]/g,
    (digit) => BENGALI_DIGIT_TO_ARABIC[digit],
  );
  const match = arabic.match(/(\d+)/);
  const hours = match ? Number(match[1]) : null;
  if (hours === null) return publishedAt;
  return hours === 1 ? "1 hour ago" : `${hours} hours ago`;
}
